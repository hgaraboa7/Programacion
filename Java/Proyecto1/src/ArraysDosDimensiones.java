
public class ArraysDosDimensiones {

	public static void main(String[] args) {
	
		int [][] manolo= {
				{10,3,5,78,9},
				{87,94,3,2,5},
				{9,-43,-2,-95,9},
				{4,56,75,32,1}
		};
		
	
		
		
		for (int []fila:manolo) {
		
			System.out.println();
			
			for(int z: fila){
				
				System.out.print(z + " ");
				
			}
			
			
		}
		
		
		
	}

}
